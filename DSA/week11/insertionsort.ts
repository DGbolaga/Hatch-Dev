//insertion sort.
//arr = [3, 4, 2, 1, 5, 9]
function insertionSort(arr: number[]) {
    for (let i = 1; i < arr.length; i++) {
        let key = arr[i];
        let j = i - 1;
        while (j >= 0 && arr[j] > key) {
            arr[j + 1] = arr[j];
            j--;
        }
        arr[j + 1] = key;
    }
}